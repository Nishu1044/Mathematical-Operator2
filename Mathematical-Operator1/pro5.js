
let day = "sun";
if (day == "sun") {
  console.log("sunday");
}
else if (day == "mon") {
  console.log("monday");
}
else if (day == "tus") {
  console.log("tuesday");
}
else if (day == "wed") {
  console.log("wedday");
}
else if (day == "thur") {
  console.log("thursday");
}
else if (day == "fri") {
  console.log("friday");
}
else if (day == "sat") {
  console.log("satday");
}
else {
  console.log("nothing");
}